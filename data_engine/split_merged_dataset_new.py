"""
Split `raw_data/merged_dataset_new` into train/val/test.

This script takes *all* images under:
  - <source>/images/train
  - <source>/images/valid
(and any other subfolders under <source>/images)

and creates a new dataset directory with:
  - images/train, images/val, images/test
  - labels/train, labels/val, labels/test

Labels are expected in YOLO format under <source>/labels mirroring the images layout.
If a label file is missing, an empty label file is created in the output.
"""

from __future__ import annotations

import argparse
import os
import random
import shutil
from pathlib import Path
from typing import Dict, List, Tuple


IMG_EXTS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


def iter_images(images_root: Path) -> List[Path]:
    out: List[Path] = []
    for p in images_root.rglob("*"):
        if not p.is_file():
            continue
        if p.name.startswith("."):
            continue
        if p.suffix.lower() in IMG_EXTS:
            out.append(p)
    return out


def label_path_for_image(source_root: Path, img_path: Path) -> Path:
    # Convert: <source>/images/.../x.jpg -> <source>/labels/.../x.txt
    rel = img_path.relative_to(source_root / "images")
    return (source_root / "labels" / rel).with_suffix(".txt")


def safe_copy(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)


def write_empty_label(dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    dst.write_text("", encoding="utf-8")


def split_counts(n: int, train: float, val: float, test: float) -> Tuple[int, int, int]:
    if n <= 0:
        return 0, 0, 0
    train_n = int(round(n * train))
    val_n = int(round(n * val))
    test_n = n - train_n - val_n
    # Fix any rounding edge cases
    if test_n < 0:
        test_n = 0
    # Ensure total is exactly n
    total = train_n + val_n + test_n
    if total != n:
        train_n += (n - total)
    return train_n, val_n, test_n


def parse_simple_data_yaml(data_yaml: Path) -> Tuple[int, Dict[int, str]]:
    """
    Best-effort parse for `nc` and `names` from a YOLO data.yaml without PyYAML.
    Falls back to (1, {0: "license"}).
    """
    nc = 1
    names: Dict[int, str] = {0: "license"}
    if not data_yaml.exists():
        return nc, names

    lines = data_yaml.read_text(encoding="utf-8", errors="ignore").splitlines()
    for line in lines:
        s = line.strip()
        if s.startswith("nc:"):
            try:
                nc = int(s.split("nc:", 1)[1].strip())
            except Exception:
                pass

    # Parse `names:` block as:
    # names:
    #   0: license
    in_names = False
    parsed: Dict[int, str] = {}
    for line in lines:
        if line.strip().startswith("names:"):
            in_names = True
            continue
        if in_names:
            if not line.startswith("  "):
                break
            item = line.strip()
            if ":" not in item:
                continue
            k, v = item.split(":", 1)
            try:
                idx = int(k.strip())
            except Exception:
                continue
            name = v.strip().strip('"').strip("'")
            if name:
                parsed[idx] = name
    if parsed:
        names = parsed
    return nc, names


def write_data_yaml(out_root: Path, nc: int, names: Dict[int, str]) -> None:
    # Keep the same style you already use: absolute `path` + relative train/val/test.
    yaml_path = out_root / "data.yaml"
    lines: List[str] = []
    lines.append("# Auto-generated by data_engine/split_merged_dataset_new.py")
    lines.append(f"path: {out_root.resolve()}")
    lines.append("train: images/train")
    lines.append("val: images/val")
    lines.append("test: images/test")
    lines.append("")
    lines.append(f"nc: {int(nc)}")
    lines.append("names:")
    for k in sorted(names.keys()):
        lines.append(f"  {k}: {names[k]}")
    yaml_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    ap = argparse.ArgumentParser(description="Split merged_dataset_new into train/val/test")
    ap.add_argument("--source", type=str, default="raw_data/merged_dataset_new", help="Source dataset root")
    ap.add_argument("--out", type=str, default="raw_data/merged_dataset_new_split", help="Output dataset root")
    ap.add_argument("--train", type=float, default=0.8, help="Train split fraction")
    ap.add_argument("--val", type=float, default=0.1, help="Val split fraction")
    ap.add_argument("--test", type=float, default=0.1, help="Test split fraction")
    ap.add_argument("--seed", type=int, default=42, help="Shuffle seed")
    ap.add_argument("--move", action="store_true", help="Move files instead of copying (destructive)")
    args = ap.parse_args()

    source_root = Path(args.source).resolve()
    out_root = Path(args.out).resolve()

    if not (source_root / "images").exists():
        raise SystemExit(f"Missing images dir: {source_root / 'images'}")
    if not (source_root / "labels").exists():
        raise SystemExit(f"Missing labels dir: {source_root / 'labels'}")

    if args.train < 0 or args.val < 0 or args.test < 0:
        raise SystemExit("Split fractions must be >= 0")
    s = args.train + args.val + args.test
    if abs(s - 1.0) > 1e-6:
        raise SystemExit(f"Split fractions must sum to 1.0 (got {s})")

    images = iter_images(source_root / "images")
    if not images:
        raise SystemExit(f"No images found under: {source_root / 'images'}")

    rng = random.Random(args.seed)
    rng.shuffle(images)

    train_n, val_n, test_n = split_counts(len(images), args.train, args.val, args.test)
    splits = {
        "train": images[:train_n],
        "val": images[train_n : train_n + val_n],
        "test": images[train_n + val_n :],
    }

    # Create output dirs
    for split in ("train", "val", "test"):
        (out_root / "images" / split).mkdir(parents=True, exist_ok=True)
        (out_root / "labels" / split).mkdir(parents=True, exist_ok=True)

    # Copy/move files
    for split, img_list in splits.items():
        for img_path in img_list:
            dst_img = out_root / "images" / split / img_path.name
            if dst_img.exists():
                raise SystemExit(f"Filename collision in output: {dst_img}")

            src_lbl = label_path_for_image(source_root, img_path)
            dst_lbl = (out_root / "labels" / split / img_path.name).with_suffix(".txt")

            if args.move:
                dst_img.parent.mkdir(parents=True, exist_ok=True)
                shutil.move(str(img_path), str(dst_img))
            else:
                safe_copy(img_path, dst_img)

            if src_lbl.exists():
                if args.move:
                    dst_lbl.parent.mkdir(parents=True, exist_ok=True)
                    shutil.move(str(src_lbl), str(dst_lbl))
                else:
                    safe_copy(src_lbl, dst_lbl)
            else:
                # Ultralytics can train with images that have no labels; keep an empty file.
                write_empty_label(dst_lbl)

    # Emit a new data.yaml
    nc, names = parse_simple_data_yaml(source_root / "data.yaml")
    write_data_yaml(out_root, nc=nc, names=names)

    print("Done.")
    print(f"Source: {source_root}")
    print(f"Output: {out_root}")
    print(f"Counts: train={len(splits['train'])}, val={len(splits['val'])}, test={len(splits['test'])}")
    print(f"YAML: {out_root / 'data.yaml'}")


if __name__ == "__main__":
    main()

