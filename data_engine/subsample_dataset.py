"""
Subsample a YOLO dataset (train/val/test) to a smaller size.

Assumes a directory layout like:
  <source>/
    images/
      train/
      val/
      test/
    labels/
      train/
      val/
      test/
    data.yaml

Creates a new dataset directory:
  <out>/
    images/{train,val,test}
    labels/{train,val,test}
    data.yaml  (same nc/names, updated path)

For each split, a random subset of images is selected according to `--fraction`.
Labels are copied 1:1 for the kept images; images with missing labels get an empty
label file in the output.
"""

from __future__ import annotations

import argparse
import os
import random
import shutil
from pathlib import Path
from typing import Dict, List, Tuple


IMG_EXTS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


def iter_split_images(images_root: Path, split: str) -> List[Path]:
    split_dir = images_root / split
    if not split_dir.exists():
        return []
    images: List[Path] = []
    for p in split_dir.rglob("*"):
        if not p.is_file():
            continue
        if p.suffix.lower() in IMG_EXTS:
            images.append(p)
    return images


def label_path_for_image(source_root: Path, img_path: Path, split: str) -> Path:
    # <source>/images/split/x.jpg -> <source>/labels/split/x.txt
    rel = img_path.relative_to(source_root / "images" / split)
    return (source_root / "labels" / split / rel).with_suffix(".txt")


def safe_copy(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)


def write_empty_label(dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    dst.write_text("", encoding="utf-8")


def choose_k(items: List[Path], k: int, rng: random.Random) -> List[Path]:
    if k >= len(items):
        return list(items)
    return rng.sample(items, k)


def parse_simple_data_yaml(data_yaml: Path) -> Tuple[int, Dict[int, str]]:
    """
    Best-effort parse for `nc` and `names` from a YOLO data.yaml without PyYAML.
    Falls back to (1, {0: "license"}).
    """
    nc = 1
    names: Dict[int, str] = {0: "license"}
    if not data_yaml.exists():
        return nc, names

    lines = data_yaml.read_text(encoding="utf-8", errors="ignore").splitlines()
    for line in lines:
        s = line.strip()
        if s.startswith("nc:"):
            try:
                nc = int(s.split("nc:", 1)[1].strip())
            except Exception:
                pass

    # Parse `names:` block as:
    # names:
    #   0: license
    in_names = False
    parsed: Dict[int, str] = {}
    for line in lines:
        if line.strip().startswith("names:"):
            in_names = True
            continue
        if in_names:
            if not line.startswith("  "):
                break
            item = line.strip()
            if ":" not in item:
                continue
            k, v = item.split(":", 1)
            try:
                idx = int(k.strip())
            except Exception:
                continue
            name = v.strip().strip('"').strip("'")
            if name:
                parsed[idx] = name
    if parsed:
        names = parsed
    return nc, names


def write_data_yaml(out_root: Path, nc: int, names: Dict[int, str]) -> None:
    yaml_path = out_root / "data.yaml"
    lines: List[str] = []
    lines.append("# Auto-generated by data_engine/subsample_dataset.py")
    lines.append(f"path: {out_root.resolve()}")
    lines.append("train: images/train")
    lines.append("val: images/val")
    lines.append("test: images/test")
    lines.append("")
    lines.append(f"nc: {int(nc)}")
    lines.append("names:")
    for k in sorted(names.keys()):
        lines.append(f"  {k}: {names[k]}")
    yaml_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    ap = argparse.ArgumentParser(description="Subsample a YOLO dataset (train/val/test) by a given fraction")
    ap.add_argument(
        "--source",
        type=str,
        default="raw_data/merged_dataset_new_split",
        help="Source dataset root (with images/{train,val,test}, labels/{train,val,test})",
    )
    ap.add_argument(
        "--out",
        type=str,
        default="raw_data/merged_dataset_new_split_half",
        help="Output dataset root",
    )
    ap.add_argument(
        "--fraction",
        type=float,
        default=0.5,
        help="Fraction of images to keep in each split (0 < f <= 1)",
    )
    ap.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for subsampling",
    )
    ap.add_argument(
        "--move",
        action="store_true",
        help="Move files instead of copying (destructive to source)",
    )
    args = ap.parse_args()

    if not (0.0 < args.fraction <= 1.0):
        raise SystemExit(f"--fraction must be in (0, 1], got {args.fraction}")

    source_root = Path(args.source).resolve()
    out_root = Path(args.out).resolve()

    images_root = source_root / "images"
    labels_root = source_root / "labels"

    if not images_root.exists() or not labels_root.exists():
        raise SystemExit(f"Source must contain images/ and labels/ dirs: {source_root}")

    rng = random.Random(args.seed)

    splits = ("train", "val", "test")
    kept_counts: Dict[str, int] = {}

    # Create output dir structure
    for split in splits:
        (out_root / "images" / split).mkdir(parents=True, exist_ok=True)
        (out_root / "labels" / split).mkdir(parents=True, exist_ok=True)

    for split in splits:
        img_list = iter_split_images(images_root, split)
        if not img_list:
            kept_counts[split] = 0
            continue

        k = max(1, int(round(len(img_list) * args.fraction)))
        chosen = choose_k(img_list, k, rng)
        kept_counts[split] = len(chosen)

        for img_path in chosen:
            dst_img = out_root / "images" / split / img_path.name
            if dst_img.exists():
                raise SystemExit(f"Filename collision in output: {dst_img}")

            src_lbl = label_path_for_image(source_root, img_path, split)
            dst_lbl = (out_root / "labels" / split / img_path.name).with_suffix(".txt")

            if args.move:
                dst_img.parent.mkdir(parents=True, exist_ok=True)
                shutil.move(str(img_path), str(dst_img))
            else:
                safe_copy(img_path, dst_img)

            if src_lbl.exists():
                if args.move:
                    dst_lbl.parent.mkdir(parents=True, exist_ok=True)
                    shutil.move(str(src_lbl), str(dst_lbl))
                else:
                    safe_copy(src_lbl, dst_lbl)
            else:
                write_empty_label(dst_lbl)

    # Copy nc/names into new data.yaml
    nc, names = parse_simple_data_yaml(source_root / "data.yaml")
    write_data_yaml(out_root, nc=nc, names=names)

    print("Done subsampling dataset.")
    print(f"Source: {source_root}")
    print(f"Output: {out_root}")
    for split in splits:
        print(f"  {split}: kept {kept_counts.get(split, 0)} images")
    print(f"YAML: {out_root / 'data.yaml'}")


if __name__ == "__main__":
    main()

